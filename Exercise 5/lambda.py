# Define lambda functions
circumference = lambda radius: 2 * 3.142 * radius
area = lambda radius: 3.142 * radius * radius

# Change the radius to a different value
new_radius = 8

# Calculate and print the circumference and area with the new radius
print("Circumference:", circumference(new_radius))
print("Area:", area(new_radius))
